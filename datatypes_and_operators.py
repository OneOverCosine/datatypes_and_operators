# Built in methods for booleans
greeting = "hello world!"
# isalpha() - returns True if the String contains ONLY letters
print(greeting.isalpha()) # returns False as the space and ! are not letters

# islower() - returns True if the letter characters in the String are all lowercase
print(greeting.islower())

